# List of numbers
numbers = [10, 20, 30, 40, 50]

# Finding the maximum value
max_value = max(numbers)
print("Maximum:", max_value)  # Output: Maximum: 50

# Finding the minimum value
min_value = min(numbers)
print("Minimum:", min_value)  # Output: Minimum: 10

# Finding the mean value
# List of numbers
numbers = [10, 20, 30, 40, 50]

# Calculate the sum of the numbers
total_sum = sum(numbers)  # total_sum is 150

# Calculate the number of elements in the list
count = len(numbers)  # count is 5

# Calculate the mean (average) value
mean_value = total_sum / count  # mean_value is 150 / 5, which is 30.0

# Print the mean value
print("Mean:", mean_value)  # Output: Mean: 30.0
